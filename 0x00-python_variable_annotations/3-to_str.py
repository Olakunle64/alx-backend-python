#!/usr/bin/env python3
"""This module has a function named <to_str> that takes
    in a float arg and return the string representation of
    the float.
"""


def to_str(n: float) -> str:
    """This function takes in a float arg and return
        the string representation of the float arg

        Args:
            n: float

        Return: return the string representation of n
    """
    return str(n)

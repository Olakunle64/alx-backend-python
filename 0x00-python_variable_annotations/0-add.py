#!/usr/bin/python3
"""This module has a function named <add> that add two floats and
    return a float as the result of the addition
"""


def add(a: float, b: float) -> float:
    """This function takes in two floats argument and
        return the addition of the the two

        Args:
            a: float
            b: float

        Return: return the addition of the two floats
    """
    return float(a + b)
